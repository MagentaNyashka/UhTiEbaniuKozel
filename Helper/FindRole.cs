using RPM.Model;
using System.Windows;
using RPM.ViewModel; 
namespace RPM.Helper 
{
    public class FindRole
    {
        int id;
        public FindRole(int id)
        {
            this.id = id;
        }
        public bool RolePredicate(Role role)
        {
            return role.Id == id;
        }
    }
}